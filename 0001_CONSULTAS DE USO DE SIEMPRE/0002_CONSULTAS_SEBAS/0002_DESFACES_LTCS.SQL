FROM SINCRONIZADOR.MONEDERO t1
         join monederoconsultas.ESTADODECUENTA MCE on t1.INUMEROTARJETA = MCE.INUMTARJETA

where t1.ICONTADORRECARGAAPLICADA > t1.ICONTADORRECARGA
  and MCE.UIDESTATUSTARJETA = '1f752e96-0ecc-4765-b163-259865bfabf0'
  and MCE.UIDESTATUS = '5e879806-6e1d-4d5b-9610-25f2a681b637'
