014.04 DATA REPORTE DE RECAUDO DE RUTAS DIARIO - MENSUAL ENERO

VALIDADOR,RUTA,ORIGEN,FECHAOPERACION,HORAOPERACION,EMPRESA,MONEDERO,NUMTARJETA,MONTO,TRX,TRANSBORDO,TIPOTARIFA

***********************************++
SELECT VALIDADOR, RUTA,NUMCOMERCIAL, FECHAOPERACION,TIPOTARIFA,sum(MONTOCOBRADO)MONTOCOBRADO, sum(NUMTRX)NUMTRX
FROM vwAbordos
WHERE YEAR(FECHAOPERACION) = 2025 AND MONTH(FECHAOPERACION) = 6 AND EMPRESA IS NOT NULL AND RUTA IS NOT NULL
GROUP BY VALIDADOR, RUTA,NUMCOMERCIAL, FECHAOPERACION,TIPOTARIFA
*************************************


SELECT VALIDADOR, RUTA,NUMCOMERCIAL, FECHAOPERACION,TIPOTARIFA,sum(MONTOCOBRADO)MONTOCOBRADO, sum(NUMTRX)NUMTRX
FROM vwAbordos
WHERE YEAR(FECHAOPERACION) = [REPORTE_ANIO] AND MONTH(FECHAOPERACION) = [REPORTE_MES] AND EMPRESA IS NOT NULL AND RUTA IS NOT NULL
GROUP BY VALIDADOR, RUTA,NUMCOMERCIAL, FECHAOPERACION,TIPOTARIFA