014.07 DATA REPORTE DE CONSULTA RAPIDA COMERCIOS ENERO

select TIPOTRANSACCION AS TIPOCARGO
, GRUPOCOMERCIAL AS COMERCIO
, FECHA AS FECHAOPERACION
, HORA AS HORAOPERACION
, TIPOMONEDERO AS TIPOTARJETA
, INUMMONEDERO AS MONEDERO
, FOLIO AS FOLIOCOMERCIO
, MONTO AS MONTO
, NUMTRX AS TRX
, METODO AS DATA1
 from [dbo].[vwIngresosCargos]
where YEAR(fecha) = 2025 and month(fecha) = 6 and TIPOMOVIMIENTO = 'Cargo'


