014.07 DATA REPORTE DE CONSULTA RAPIDA DEBITACIONES ENERO

VALIDADOR,RUTA,ORIGEN,FECHAOPERACION,HORAOPERACION,EMPRESA,MONEDERO,NUMTARJETA,MONTO,TRX,TRANSBORDO,TIPOTARIFA


CONSULTA

*****************************************
SELECT VALIDADOR,
RUTA,
ORIGEN,
FECHAOPERACION,
HORAOPERACION,
EMPRESA,
SNUMEROMONEDERO AS MONEDERO,
SNOTARJETA AS NUMTARJETA
,MONTOCOBRADO AS MONTO
,NUMTRX AS TRX
,NUMTRANSBORDO  AS TRANSBORDO
,TIPOTARIFA AS TIPOTARIFA
FROM vwAbordos WITH (NOLOCK)
WHERE YEAR(FECHAOPERACION) = 2025 AND MONTH(FECHAOPERACION) = 6 AND EMPRESA IS NOT NULL AND RUTA IS NOT NULL
*************************************

SELECT VALIDADOR,
RUTA,
ORIGEN,
FECHAOPERACION,
HORAOPERACION,
EMPRESA,
SNUMEROMONEDERO AS MONEDERO,
SNOTARJETA AS NUMTARJETA
,MONTOCOBRADO AS MONTO
,NUMTRX AS TRX
,NUMTRANSBORDO  AS TRANSBORDO
,TIPOTARIFA AS TIPOTARIFA
FROM vwAbordos WITH (NOLOCK)
WHERE YEAR(FECHAOPERACION) = [REPORTE_ANIO] AND MONTH(FECHAOPERACION) = [REPORTE_MES] AND EMPRESA IS NOT NULL AND RUTA IS NOT NULL
