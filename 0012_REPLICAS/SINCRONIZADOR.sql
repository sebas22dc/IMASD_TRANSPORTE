	SELECT 
	 U4.INUMMONEDERO ,
	U2.DSALDO AS TICKETS,U2.SESTATUS  ,
	U3.DSALDO AS MONEDERO,U3.SESTATUS,
	U4.DSALDO  AS MQ_SALDO,
	U5.DSALDO  AS MC_SALDO,
	u6.DSALDO  AS PAGOS,
	U3.UIDESTATUS AS UIESTATUSCUENTA_MONEDERO,U1.UIDESTADODECUENTA
	FROM  PREP_APP.ESTADODECUENTA U1
	INNER JOIN PREP_APPTICKETS.ESTADODECUENTA U2 ON U1.UIDESTADODECUENTA=U2.UIDESTADODECUENTA 
	INNER JOIN PREP_MONEDEROCONSULTAS.ESTADODECUENTA U3 ON U1.UIDESTADODECUENTA =U3.UIDESTADODECUENTA
	INNER JOIN PREP_APPMONEDEROQUERY.ESTADODECUENTA U4 ON U1.UIDESTADODECUENTA =U4.UIDESTADODECUENTA
	INNER JOIN PREP_APPMONEDEROCOMMAND.ESTADODECUENTA U5 ON U1.UIDESTADODECUENTA =U5.UIDESTADODECUENTA
	INNER JOIN PREP_PAGOS.ESTADODECUENTA U6 ON U1.UIDESTADODECUENTA =U6.UIDESTADODECUENTA
	WHERE U1.UIDMONEDERO IN ( SELECT  M.UIDMONEDERO FROM  PREP_SINCRONIZADOR.MONEDERO m WHERE m.INUMEROTARJETA IN ('9899982186677318','9899982186677319'))
	ORDER  BY U1.DTFECHAULTIMAOPERACION DESC

*Movimientos de las tarjetas

	SELECT c.DTFECHACREACION ,c.* FROM PREP_SINCRONIZADOR.CAMBIOSMONEDERO c
	INNER JOIN PREP_SINCRONIZADOR.MONEDERO m ON c.STARJETA =m.INUMEROTARJETA
	WHERE m.UIDMONEDERO IN 
	(
		SELECT  M.UIDMONEDERO FROM  PREP_SINCRONIZADOR.MONEDERO m WHERE m.INUMEROTARJETA IN ('9899982186677318','9899982186677319')
	)
	AND
	c.DTFECHACREACION >TO_DATE('15-06-2025 00:00:00', 'DD-MM-YYYY HH24:MI:SS') 
	AND    
	c.DTFECHACREACION <TO_DATE('15-07-2025 23:59:59', 'DD-MM-YYYY HH24:MI:SS')
	ORDER BY m.INUMEROTARJETA ,c.DTFECHACREACION DESC



