---- Catalogos.AutobusRutaChofer 

	SELECT a.UIDAUTOBUSRUTACHOFER,a.UIDCHOFER ,a2.SPLACAS,r.SRUTA,a.BACTIVO,a.BBAJA ,a.* FROM PREP_SINCRONIZADOR.AUTOBUSRUTACHOFER a 
