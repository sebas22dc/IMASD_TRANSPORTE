---- Catalogos.ValidadorVsam
	
	SELECT val.SCLAVE  , vsam.SCSN,val.SCLAVE,val.SNUMEROSERIE,val.SVALIDADOR,vvsam2.BACTIVO,vvsam2.BBAJA,vvsam2.ISLOT ,vvsam2.DTFECHACREACION ,vvsam2.DTFECHAULTMOD ,vvsam2.DTFECHABAJA 
	FROM PREP_SINCRONIZADOR.VALIDADORES val 
	INNER JOIN PREP_SINCRONIZADOR.VALIDADORVSAM vvsam2 ON val.UIDVALIDADOR =vvsam2.UIDVALIDADOR 
	INNER  JOIN PREP_SINCRONIZADOR.VSAM vsam ON vsam.UIDVSAM =vvsam2.UIDVSAM 
	ORDER BY vvsam2.DTFECHACREACION DESC;
	

